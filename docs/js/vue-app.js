// ============================================================
// STORE — Reactive Global State
// ============================================================
const store = reactive({
  data: null,
  loading: true,
  error: null,
  today: new Date().toISOString().slice(0,10),

  async init() {
    try {
      const urls = ['data.json', 'index.json'];
      for (const url of urls) {
        try {
          const r = await fetch(url+'?'+Date.now());
          if (r.ok) { this.data = await r.json(); break; }
        } catch(e) {}
      }
      if (!this.data) this.data = { questions:[], categories:{}, top_tags:[], dates:[], by_date:{} };
      this.loading = false;
    } catch(e) {
      this.error = e.message;
      this.loading = false;
    }
  },

  get questions() { return this.data?.questions || []; },
  get categories() { return this.data?.categories || {}; },
  get topTags() { return this.data?.top_tags || []; },

  // localStorage review helpers
  getReviewed(date) {
    try { return JSON.parse(localStorage.getItem('reviewed_'+date)||'[]'); }
    catch { return []; }
  },
  setReviewed(date, ids) { localStorage.setItem('reviewed_'+date, JSON.stringify(ids)); },
  toggleReview(qid, date) {
    date = date || this.today;
    const r = this.getReviewed(date);
    const i = r.indexOf(qid);
    i>=0 ? r.splice(i,1) : r.push(qid);
    this.setReviewed(date, r);
    return i<0;
  },
  isReviewed(qid, date) { return this.getReviewed(date||this.today).includes(qid); },
  countReviewed(date) { return this.getReviewed(date||this.today).length; },
  getStreak() {
    let s=0, d=new Date();
    if (this.countReviewed(this.today)===0) d.setDate(d.getDate()-1);
    while(true) {
      if (this.getReviewed(d.toISOString().slice(0,10)).length>0) { s++; d.setDate(d.getDate()-1); }
      else break;
    }
    return s;
  },

  // Data helpers
  catInfo(key) { return this.categories[key] || { name: key||'未分类', icon:'📌' }; },
  diffName(d) { return {easy:'基础',medium:'中级',hard:'进阶'}[d]||d; },
  diffEmoji(d) { return {easy:'🟢',medium:'🟡',hard:'🔴'}[d]||'⚪'; },
  diffColor(d) { return {easy:'#3fb950',medium:'#d2991d',hard:'#f85149'}[d]||'#8b949e'; },
  esc(s) { if(!s)return''; const d=document.createElement('div');d.textContent=s;return d.innerHTML; },
  truncate(s,n=80) { return s&&s.length>n ? s.slice(0,n)+'...' : s||''; },
  dateLabel(d) {
    if(!d) return ''; if(d===this.today) return '今天';
    const y=new Date(Date.now()-86400000).toISOString().slice(0,10);
    if(d===y) return '昨天';
    const w=['周日','周一','周二','周三','周四','周五','周六'];
    return d.slice(5)+' '+w[new Date(d+'T00:00:00').getDay()];
  },

  getCatCounts() {
    const c={}; this.questions.forEach(q=>{c[q.category||'?']=(c[q.category||'?']||0)+1;});
    return c;
  },
  getTagCounts() {
    const c={}; this.questions.forEach(q=>{(q.tags||[]).forEach(t=>{c[t]=(c[t]||0)+1;});});
    return c;
  },
  filterQuestions({category,difficulty,tag,search,sort,limit}={}) {
    let qs=[...this.questions];
    if(category) qs=qs.filter(q=>q.category===category);
    if(difficulty) qs=qs.filter(q=>q.difficulty===difficulty);
    if(tag) qs=qs.filter(q=>(q.tags||[]).includes(tag));
    if(search){const kw=search.toLowerCase();qs=qs.filter(q=>q.question.toLowerCase().includes(kw)||(q.tags||[]).some(t=>t.toLowerCase().includes(kw)));}
    if(sort==='oldest') qs.sort((a,b)=>(a.created||'').localeCompare(b.created||''));
    else if(sort==='difficulty'){const o={easy:0,medium:1,hard:2};qs.sort((a,b)=>(o[a.difficulty]||1)-(o[b.difficulty]||1));}
    else qs.sort((a,b)=>(b.created||'').localeCompare(a.created||''));
    if(limit) qs=qs.slice(0,limit);
    return qs;
  },
  getQuestion(qid) { return this.questions.find(q=>q.id===qid); },
});

